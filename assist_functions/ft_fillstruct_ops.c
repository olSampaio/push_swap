#include "../push_swap.h"

void	ft_fillstruct_ops(t_operations *ops)
{
	ops->pb = 0;
	ops->rra = 0;
	ops->rrb = 0;
	ops->rrr = 0;
	ops->ra = 0;
	ops->rb = 0;
	ops->rr = 0;
	ops->sa = 0;
	ops->sb = 0;
	ops->ss = 0;
	ops->pa = 0;
}
